package com.company;

public class cars {
    String name;
    String color;
    Integer cost;

    public cars(String name, String color, Integer cost){
        this.name = name;
        this.color = color;
        this.cost = cost;

    }

    public String getName() {
        return name;
    }



    public String getColor() {
        return color;
    }



    public Integer getCost() {
        return cost;
    }
}